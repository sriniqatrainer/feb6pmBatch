package com.BasicJava;

public class VariableExample {
		
	public static void main(String[]args)
	{
		
		// Syntax: dataType variableName = value;
		
		// a variable is a Temp memory
		// a variable should be assigned with a value
		// a variable should be declared with a dataType
		// a variable once declared with a dataType need not be declared again thoughOut the Program 
		// a variable can hold only one value at a time
		// a variable can only hold similar dataType values
		
		int var1=10;
		
		var1=20;
		
		System.out.println(" The value of the variable var1 is : " + var1);
		
		var1=30;
		
		System.out.println(" The value of the variable var1 is : " + var1);
		
		String var2="LiveTech";
		
		System.out.println(" The value of the variable var2 is : "+ var2 );
		
		String var3="101";
		
		System.out.println(" The value of the variable var3 is : "+ var3 );
		
		char var4='A';
				
		System.out.println(" The value of the variable var4 is : "+ var4 );
		
		double var5=12.345;
		
		System.out.println(" The value of the variable var5 is : "+ var5 );
		
		float var6=456;
		
		System.out.println(" The value of the variable var6 is : "+ var6 );
				
	}
}
